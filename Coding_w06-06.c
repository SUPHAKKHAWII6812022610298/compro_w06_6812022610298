#include <stdio.h>

int main() {
    int a = 5, b = 2;
    float x = 3.0, y = 4.5;
    int r1, r2;
    float r3, r4;

    // r1 = a++ * b + (int)y % 3;
    // a = 5 (ใช้ก่อนเพิ่ม), b = 2, (int)y = 4 → 4 % 3 = 1
    // r1 = 5 * 2 + 1 = 10 + 1 = 11, แล้ว a = 6
    r1 = a++ * b + (int)y % 3;

    // r2 = (a > b) && ((int)x / b < 2);
    // a = 6 > 2 → 1 (true), (int)x = 3 → 3 / 2 = 1 < 2 → 1 (true)
    // r2 = 1 && 1 = 1
    r2 = (a > b) && ((int)x / b < 2);

    // r3 = ++x * y - a / 2;
    // ++x = 4.0, y = 4.5 → 4.0 * 4.5 = 18.0, a = 6 → 6 / 2 = 3
    // r3 = 18.0 - 3 = 15.0
    r3 = ++x * y - a / 2;

    // r4 = ((x += 1.5) > y) || (b-- > 0);
    // x = 4.0 + 1.5 = 5.5 > y = 4.5 → 1 (true)
    // ใช้ || → short-circuit → b-- ไม่ถูกเรียก
    // r4 = 1.0
    r4 = ((x += 1.5) > y) || (b-- > 0);

    // แสดงผลลัพธ์
    printf("r1 = %d\n", r1);   // 11
    printf("r2 = %d\n", r2);   // 1
    printf("r3 = %.2f\n", r3); // 15.00
    printf("r4 = %.2f\n", r4); // 1.00

    return 0;
}