#include <stdio.h>

int main() {
    int x = 12;
    int y = 7;
    int z = 12;

    printf("W06-03: ตัวอย่างการเปรียบเทียบและตรรกะ\n\n");

    // 1. x > y
    printf("1. x > y = %d\n", x > y); // 1 (12 > 7)

    // 2. x < z
    printf("2. x < z = %d\n", x < z); // 0 (12 < 12 = false)

    // 3. x == z
    printf("3. x == z = %d\n", x == z); // 1 (12 == 12)

    // 4. x != y
    printf("4. x != y = %d\n", x != y); // 1 (12 != 7)

    // 5. ตรวจสอบนิพจน์เชิงตรรกะ
    // !(2 * 5 >= y) || (5 != (5 / 3)) = !(10 >= 7) || (5 != 1) = 0 || 1 = 1
    printf("5. !(2 * 5 >= y) || (5 != (5 / 3)) = %d\n", !(2 * 5 >= y) || (5 != (5 / 3)));

    // 6. !(x < y)
    printf("6. !(x < y) = %d\n", !(x < y)); // !(12 < 7) = !(0) = 1

    // 7. (x + y) > (z * 2)
    printf("7. (x + y) > (z * 2) = %d\n", (x + y) > (z * 2)); // 19 > 24 = 0

    // 8. ตรวจสอบว่า x เป็นเลขคู่ หรือ y เป็นเลขคี่
    // x % 2 == 0 -> true, y % 2 == 1 -> true => true || true = true
    printf("8. (x %% 2 == 0) || (y %% 2 == 1) = %d\n", (x % 2 == 0) || (y % 2 == 1));

    // 9. x > y && z < y
    // (12 > 7) && (12 < 7) = 1 && 0 = 0
    printf("9. (x > y) && (z < y) = %d\n", (x > y) && (z < y));

    return 0;
}